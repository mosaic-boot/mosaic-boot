package io.mosaicboot.core.util

import jakarta.servlet.http.HttpServletRequest
import org.springframework.core.MethodParameter
import org.springframework.web.bind.support.WebDataBinderFactory
import org.springframework.web.context.request.NativeWebRequest
import org.springframework.web.method.support.HandlerMethodArgumentResolver
import org.springframework.web.method.support.ModelAndViewContainer

class WebClientInfoResolver : HandlerMethodArgumentResolver {
    companion object {
        fun fromHttpServletRequest(request: HttpServletRequest): WebClientInfo {
            return WebClientInfo(
                host = request.getHeader("Host"),
                ipAddress = request.remoteAddr,
                userAgent = request.getHeader("User-Agent") ?: "",
                referer = request.getHeader("Referer") ?: "",
            )
        }
    }

    override fun supportsParameter(parameter: MethodParameter): Boolean {
        return parameter.parameterType == WebClientInfo::class.java
    }

    override fun resolveArgument(
        parameter: MethodParameter,
        mavContainer: ModelAndViewContainer?,
        webRequest: NativeWebRequest,
        binderFactory: WebDataBinderFactory?
    ): Any? {
        val request = webRequest.getNativeRequest(HttpServletRequest::class.java)
            ?: return null
        return fromHttpServletRequest(request)
    }
}